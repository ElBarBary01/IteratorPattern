
public class EmployeeInfo {
	String name;
	String role;
	int salary;
	
	public EmployeeInfo(String name, String role, int salary) {
		super();
		this.name = name;
		this.role = role;
		this.salary = salary;
	}
	public String getName() {
		return name;
	}
	
	public String getRole() {
		return role;
	}
	
	public int getSalary() {
		return salary;
	}
	
	
}
